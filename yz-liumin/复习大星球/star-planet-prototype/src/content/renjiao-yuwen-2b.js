export function buildReviewTasks(lesson) {
  const recognition = lesson.recognitionChars || [];
  const charQuestions = recognition.slice(0, 4).map((item, index) => {
    const distractors = ["yóu", "dì", "guǒ", "jú", "qiǎo", "lǐ", "xiān", "jì"].filter((pinyin) => pinyin && pinyin !== item.pinyin);
    return {
      char: item.char,
      stem: "这个字读什么？",
      options: [item.pinyin, ...distractors.slice(index, index + 2)].slice(0, 3),
      answer: item.pinyin,
    };
  });

  const words = lesson.words?.length ? lesson.words : lesson.writingChars;
  const quizQuestions = [
    {
      stem: `“${lesson.writingChars[0]}”可以组成哪个教材词语？`,
      options: [words[0], words[1] || "春天", words[2] || "礼物"].filter(Boolean),
      answer: words[0],
    },
    {
      stem: `下面哪个词语出自《${lesson.title}》？`,
      options: [words.at(-1), "冰山", "雪人"].filter(Boolean),
      answer: words.at(-1),
    },
  ];

  const readTask = charQuestions.length
    ? { id: "chars", title: "生字认读", minutes: 3, tone: "gold", kind: "charQuiz", questions: charQuestions }
    : { id: "chars", title: "生字认读", minutes: 3, tone: "gold", kind: "readCard", chars: lesson.writingChars };

  return [
    readTask,
    { id: "pinyin", title: "词语选择", minutes: 3, tone: "blue", kind: "quiz", questions: quizQuestions },
    { id: "reading", title: "朗读挑战", minutes: 4, tone: "violet", kind: "reading", text: lesson.readingText || `请朗读《${lesson.title}》中老师布置的自然段。` },
    { id: "wrong", title: "错题宝箱", minutes: 5, tone: "coral", kind: "wrong" },
  ];
}

export function buildGeneratedExam(lesson) {
  const words = lesson.words?.length ? lesson.words : lesson.writingChars;
  const chars = lesson.writingChars;
  return [
    { stem: `《${lesson.title}》属于哪一课？`, options: [lesson.shortTitle, "第1课《古诗二首》", "第8课《彩色的梦》"], answer: lesson.shortTitle },
    { stem: `“${chars[0]}”可以组成哪个词语？`, options: [words[0], "冰山", "雪人"], answer: words[0] },
    { stem: `下面哪个词语来自《${lesson.title}》？`, options: [words[1] || words[0], "动画片", "铅笔刀"], answer: words[1] || words[0] },
    { stem: `本课写字表中有哪个字？`, options: [chars[1] || chars[0], "森", "雷"], answer: chars[1] || chars[0] },
    { stem: `下面哪个是本课词语表里的词？`, options: [words.at(-1), "海边", "城堡"], answer: words.at(-1) },
    { stem: `本课所在单元是？`, options: [lesson.unit, "第八单元·阅读", "第三单元·识字"], answer: lesson.unit },
    { stem: `本课页码从教材第几页开始？`, options: [`第${lesson.page}页`, "第1页", "第100页"], answer: `第${lesson.page}页` },
    { stem: `家长选择的复习范围是？`, options: [lesson.shortTitle, "整本教材", "课外阅读"], answer: lesson.shortTitle },
  ];
}
